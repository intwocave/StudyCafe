package ch06.sec15;

public class SingletonExample {
	public static void main(String[] args) {
		Singleton a = Singleton.getInstance();
		Singleton b = Singleton.getInstance();
		
		if (a == b) {
			System.out.println("Singleton 타입의 변수 a와 b가 참조하는 주소가 같습니다.");
		} else {
			System.out.println("Singleton 타입의 변수 a와 b가 참조하는 주소가 다릅니다.");
		}
	}
}
